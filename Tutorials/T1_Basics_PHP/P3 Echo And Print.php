<?php

//without parentheses ()
echo "Hello I AM Echo <br>";
print "Hello I Am Print <br>";

//with parentheses ()
echo ("Hello I am Echo with () <br>");
print ("Hello I am Print with () <br>");


/*
1. echo and print are more or less the same. They are both used to output data to the screen
2. echo has no return value while print has a return value of 1 so it can be used in expressions
3. echo can take multiple parameters (although such usage is rare) while print can take one argument
4. echo is marginally faster than print
*/
